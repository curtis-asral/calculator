def add(x, y):
    return x + y


def subtract(x, y):
    return x - y


def multiply(x, y):
    return x * y


def divide(x, y):
    return x / y


def power(x, y):
    return x**y


def root(x, y):
    return x ** (1 / y)


def modulus(x, y):
    return x % y


def integer_divide(x, y):
    return int(x / y)


def percent(x, y):
    return (x / y) * 100


def absolute_difference(x, y):
    return abs(x - y)
